package se.marten.apps.orion.entities;

public class GroceryCategory {
	
	public GroceryCategory(){}
	
	private Long id;
	private String name;
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
}
